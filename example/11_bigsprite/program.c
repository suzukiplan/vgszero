void main(void)
{
    // パレットを初期化
    init_palette();

    // Bank 2 を Sprite パターンとして利用
    *VGS0_ADDR_SPRITE_DPM = 2;

    // グローバル変数を初期化
    vgs0_memset(0xC000, 0, sizeof(GlobalVariables));

    // メインループ
    uint8_t n = 0;
    uint8_t r = 0;
    while (1) {
        // V-BLANK を待機
        vgs0_wait_vsync();

        // 爆発を追加
        if (0 == GV->obj[n].flag) {
            GV->obj[n].flag = 1;
            if (n & 7) {
                GV->obj[n].big = 0;
            } else {
                GV->obj[n].big = 1;
            }
            GV->obj[n].an = 0;
            GV->obj[n].pi = 0;
            GV->obj[n].x = random[r];
            r++;
            GV->obj[n].y = random[r] % 192;
            r++;
            if (GV->obj[n].big) {
                vgs0_oam_set(n, GV->obj[n].x, GV->obj[n].y, 0x80 + ((n & 1) * 0x20), 0, 11, 11);
                VGS0_ADDR_OAM[n].bank = 3;
            } else {
                vgs0_oam_set(n, GV->obj[n].x, GV->obj[n].y, 0x80 + ((n & 1) * 0x20), ptnnum[0], 2, 2);
            }
            n++;
        }

        // スプライトを4フレームに1回アニメーション
        uint8_t i = 0;
        do {
            if (GV->obj[i].flag) {
                GV->obj[i].an++;
                GV->obj[i].an &= 0x03;
                if (0 == GV->obj[i].an) {
                    if (GV->obj[i].big) {
                        // 12x12 の超スプライトはバンク切り替えでアニメーション
                        VGS0_ADDR_OAM[i].bank += 1;
                        if (17 < VGS0_ADDR_OAM[i].bank) {
                            // 削除
                            GV->obj[i].flag = 0;
                            VGS0_ADDR_OAM[i].attr = 0;
                        }
                    } else {
                        // 3x3 の巨大スプライトはパターン切り替えでアニメーション
                        GV->obj[i].pi++;
                        if (GV->obj[i].pi < 10) {
                            VGS0_ADDR_OAM[i].ptn = ptnnum[GV->obj[i].pi];
                        } else {
                            // 削除
                            GV->obj[i].flag = 0;
                            VGS0_ADDR_OAM[i].attr = 0;
                        }
                    }
                }
            }
            i++;
        } while (i);
    }
}